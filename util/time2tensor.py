# 将时间str转换为tensor
import torch
class to_tensor():
    def __call__(self, timelist : list) -> torch.Tensor:
        timels_tensor = []
        for time in timelist:
            timels_tensor.append([float(time[4:6])])
        return torch.as_tensor(timels_tensor)

